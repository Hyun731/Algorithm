wl = int(input())

if 380 <= wl < 425:
    print("Violet")
elif 425 <= wl < 450:
    print("Indigo")
elif 450 <= wl < 495:
    print("Blue")
elif 495 <= wl < 570:
    print("Green")
elif 570 <= wl < 590:
    print("Yellow")
elif 590 <= wl < 620:
    print("Orange")
elif 620 <= wl <= 780:
    print("Red")